package project.jump.dto;

public class JumpDTO {
	private String jumpID;
	private String jumpPwd;
	private String jumpName;
	private String jumpPhone;
	public String getJumpID() {
		return jumpID;
	}
	public void setJumpID(String jumpID) {
		this.jumpID = jumpID;
	}
	public String getJumpPwd() {
		return jumpPwd;
	}
	public void setJumpPwd(String jumpPwd) {
		this.jumpPwd = jumpPwd;
	}
	public String getJumpName() {
		return jumpName;
	}
	public void setJumpName(String jumpName) {
		this.jumpName = jumpName;
	}
	public String getJumpPhone() {
		return jumpPhone;
	}
	public void setJumpPhone(String jumpPhone) {
		this.jumpPhone = jumpPhone;
	}
}
